#The underscores make the numbers easier to read but do not affect the numbers themselves
num1 = 10_000_000_000
num2 = 100_000_000

total = num1 + num2

#We can format the number to show separators
print(f'{total:,}')

#formatiing the decimal places
num1 = 3.1454625464
num2 = 10.29654635

print(f'num 1 is {num1:.3} and num2  is {num2:.2f}')

